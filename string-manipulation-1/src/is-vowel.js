function isVowel(character){
  switch (character) {
    case 'a':
      return true;
    case 'e':
      return true;
    case 'i':
      return true;
    case 'o':
      return true;
    case 'u':
      return true;
    case 'A':
      return true;
    case 'E':
      return true;
    case 'I':
      return true;
    case 'O':
      return true;
    case 'U':
      return true;
    default:
      return false;
  }
}
